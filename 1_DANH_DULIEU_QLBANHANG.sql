    GIA_1_DANH MONEY
)

--BANG HOA DON
create table HOADON_1_DANH
(
    SOHD_1_DANH INT PRIMARY KEY NOT NULL,
    NGHD_1_DANH  SMALLDATETIME,
    MAKH_1_DANH CHAR(4) REFERENCES KHACHHANG_1_DANH(MAKH_1_DANH),
    MANV_1_DANH CHAR(4) REFERENCES NHANVIEN_1_DANH(MANV_1_DANH),
    TRIGIA_1_DANH MONEY
)

--BANG CTHD
create table CTHD_1_DANH
(
    SOHD_1_DANH INT REFERENCES HOADON_1_DANH(SOHD_1_DANH) NOT NULL,
    MASP_1_DANH CHAR(4) REFERENCES SANPHAM_1_DANH(MASP_1_DANH) NOT NULL,
    PRIMARY KEY (SOHD_1_DANH, MASP_1_DANH),
